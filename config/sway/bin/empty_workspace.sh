#!/bin/bash

MAX_WORKSPACES=5

WORKSPACE_PREFIX="w_"

NUMBER_OF_CREATED_WORKSPACES=$(swaymsg -t get_workspaces | jq -r '.[] .name' | grep -c $WORKSPACE_PREFIX)

WORKSPACE_NR=$(( $NUMBER_OF_CREATED_WORKSPACES > $MAX_WORKSPACES ? $MAX_WORKSPACES : $NUMBER_OF_CREATED_WORKSPACES ))

swaymsg workspace "${WORKSPACE_PREFIX}${WORKSPACE_NR}"
