#!/bin/bash

MAX_WORKSPACES=5

WORKSPACE_PREFIX="w_"

NUMBER_OF_CREATED_WORKSPACES=$(i3-msg -t get_workspaces | tr ',' '\n' | grep -c $WORKSPACE_PREFIX)

WORKSPACE_NR=$(( $NUMBER_OF_CREATED_WORKSPACES > $MAX_WORKSPACES ? $MAX_WORKSPACES : $NUMBER_OF_CREATED_WORKSPACES ))

i3-msg workspace "${WORKSPACE_PREFIX}${WORKSPACE_NR}"
